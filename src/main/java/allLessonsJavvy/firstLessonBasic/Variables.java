package allLessonsJavvy.firstLessonBasic;

public class Variables {
    public static void main(String[] args) {

        //block
        String task = "Learn how to code";
        task = "Grab a juicebox";
        System.out.println(task);

        //block
        String love = "I love.. ";
        String thing = "carpets";
        System.out.println(love + thing);

        //block
        int first = 18;
        int second = 3;
        int result = first / second;   //you can use * + -
        System.out.println(result);
    }
}