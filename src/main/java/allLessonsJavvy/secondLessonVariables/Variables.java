package allLessonsJavvy.secondLessonVariables;

public class Variables {
    public static void main(String[]args){

        //block
        int myYear = 1987;
        myYear++;   // --
        System.out.println(myYear);

        //block
        int myYears = 1958;
        myYears += 30;  // -=
        System.out.println(myYears);
    }
}
